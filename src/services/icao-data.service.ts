@Injectable({
  providedIn: 'root'
})
export class IcaoDataService {
  
  searchAirports(term: string): Airport[] {
    if (!term || !term.trim()) {
      return [];
    }
    const lowerTerm = term.toLowerCase();
    return AIRPORTS.filter(airport =>
      airport.icao.toLowerCase().startsWith(lowerTerm) ||
      airport.name.toLowerCase().includes(lowerTerm) ||
      airport.city.toLowerCase().includes(lowerTerm)
    ).slice(0, 10);
  }

  getCategories() {
    return Object.entries(NOTAM_CATEGORIES).map(([key, value]) => ({ key, value }));
  }

  getSubjectsForCategory(categoryKey: string) {
    if (!categoryKey || !NOTAM_SUBJECTS[categoryKey]) {
      return [];
    }
    const subjects = NOTAM_SUBJECTS[categoryKey];
    return Object.entries(subjects).map(([key, value]) => ({ key, value: `${key} - ${value}` }));
  }

  getConditionsForSubject(subjectKey: string) {
    if (!subjectKey || !NOTAM_CONDITIONS[subjectKey]) {
      return [];
    }
    const conditions = NOTAM_CONDITIONS[subjectKey];
    return Object.entries(conditions).map(([key, value]) => ({ key, value: `${key} - ${value}` }));
  }

  getTemplates(): NotamTemplate[] {
    return NOTAM_TEMPLATES;
  }
  
  getTemplate(key: string): NotamTemplate | undefined {
    return NOTAM_TEMPLATES.find(t => t.key === key);
  }

  getScheduleFrequencies() {
    return SCHEDULE_FREQUENCIES;
  }
}
